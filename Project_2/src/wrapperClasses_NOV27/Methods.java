package wrapperClasses_NOV27;

public class Methods {

	public static void main(String[] args) {
		int item=Integer.valueOf("123");
		System.out.println(Integer.compare(item, 12));
		System.out.println(Integer.hashCode(item));
		System.out.println(Integer.bitCount(item));
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.highestOneBit(item));
		System.out.println(Integer.SIZE);
		System.out.println(Integer.max(item, 12));
		System.out.println(Integer.min(item, 12));
		System.out.println(Integer.parseInt("145"));
		
	}

}
