package wrapperClasses_NOV27;

public class Wrapping {

	public static void main(String[] args) {
		Integer integer=new Integer("1234");
		System.out.println(integer);
		Double double1=new Double("143.89");
		Double double2=123.67;
		System.out.println(double2);
		
		System.out.println(double1);
		Character character=new Character('v');
		System.out.println(character);
		Boolean boolean1=new Boolean(false);
		System.out.println(boolean1);
		Long long1=new Long(12637367);
		System.out.println(long1);
		Float float1=new Float("1234.45");
		System.out.println(float1);
		Short short2=new Short("1");
		System.out.println(short2);
		Byte byte1=new Byte("5");
		System.out.println(byte1);
		
      
	}

}
